Obstacle::Obstacle(Vector2D position)
{
    this->position = position;
    
    
    for(unsigned int row = 0; row < OBSTACLE_HEIGHT; ++row) {
        for(unsigned int column = 0; column < OBSTACLE_WIDTH; ++column){
            if(grid[row][column] == 1) {
                float pos_x = position.x + column *3;
                float pos_y = position.y + row * 3;
                
                blocks.push_back(ObstacleBlock({pos_x, pos_y}));
            }
        }
    }
}

void Obstacle::Draw(C2DGraphics *graphics) {

    //debugText.Format("Obstacle has %i blocks and  position: {%f, %f}", blocks.getSize() , position.x, position.y);
    //graphics->DrawText(10,200, WHITE_COLOR, debugText);

    for(int i = 0; i < blocks.getSize(); i++)
    {
        blocks[i].Draw(graphics);
    }
}

Rectangle Obstacle::GetRect()
{
    return {
        position.x,
        position.y,
        OBSTACLE_WIDTH*3,
        OBSTACLE_HEIGHT*3,
    };
}